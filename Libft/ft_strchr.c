#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	p;

	p = c;
	while (*s)
	{
		if (*s == p)
		{
			return ((char *)s);
		}
		s++;
	}
	if (p == '\0')
		return ((char *)s);
	return (0);
}
